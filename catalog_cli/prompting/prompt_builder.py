from __future__ import annotations

PROMPT_TEMPLATE = (
    'Сделай кластеризацию в виде дерева для сферы "{sphere}: {subsphere}"\n'
    "Семантическое ядро должно быть максимально полным и включать всё что касается данного бизнеса.\n"
    "Названия писать так, чтобы это подходило под ключевые запросы поисковых систем.\n"
    "Добавь недостающие услуги и не генерируй ключевые слова, а лишь добавь те, которые действительно существуют.\n"
    "Каждое название раздела или услуги должно быть оптимизировано под конкретный SEO-запрос.\n"
    "Оптимизируй категории под SEO: например, \"Продажа стекла и материалов\" сократить до \"Продажа стекла\".\n"
    "Убирай логические дубли.\n\n"
    "Формат выдачи:\n"
    "- Верни только строки формата A/B/C/D/E/F/G, каждая с новой строкой.\n"
    "- Первая часть пути ДОЛЖНА быть ровно \"{sphere}\".\n"
    "- Не добавляй пояснений, комментариев, нумерации, маркеров, пустых строк и форматирования (никаких ``` и т.п.).\n"
    "- Используй прямой слеш \"/\" как разделитель уровней.\n"
    "- Уровней может быть от 3 до 7 (минимум A/B/C).\n"
    "- Исключи дубли (с учётом регистра и пробелов).\n"
    "- Язык: русский."
)


def build_prompt(sphere: str, subsphere: str) -> str:
    return PROMPT_TEMPLATE.format(sphere=sphere.strip(), subsphere=subsphere.strip())
